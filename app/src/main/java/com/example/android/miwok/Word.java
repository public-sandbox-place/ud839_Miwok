package com.example.android.miwok;

public class Word {
    private String mName1;
    private String mName2;

    Word(String name1, String name2) {
        mName1 = name1;
        mName2 = name2;
    }

    public String getName1() {
        return mName1;
    }

    public String getName2() {
        return mName2;
    }
}
